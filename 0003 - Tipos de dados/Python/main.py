#### Tipo String
name = 'Pedro alvez barretos'
print(name)             # nome completo
print(name[0])          # Apenas a primeira letra
print(name[3:])         # A partir do 3º indice
print(name[:3])         # Até o 3º indice
print(name * 2)         # Multiplica strings
print(name + ' Carlos') # Adiciona texto


#### Tipo lista
myList = [1, 'leo', True, 12.1]
print(myList)

#### Tipo tupla
myTupla = (23, 'Leodeymison', 12,34)
print(myTupla)

#### Tipo dicionário
myDic = {1: 'Leode', 2: 2, 3: 12.4}
print(myDic)

#### Complexo
numberOctal = 0O107

print(type(numberOctal))

dados = complex("5.5+2.3j")
